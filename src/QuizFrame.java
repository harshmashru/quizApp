        String username = jTextField1.getText();
        String password = jPasswordField1.getText();
        try{
            int l=0;
            String url1 = "jdbc:derby://localhost:1527/quizapp";
            String qry = "select * from quizlog";
            con = DriverManager.getConnection(url1, "quiz", "quiz");
            st = (Statement) con.createStatement();
            rs = st.executeQuery(qry);
            while(rs.next()){
                if(rs.getString(1).equals(username)&&rs.getString(2).equals(password)){
                    l=1;
                    break;
                }
            }
            if(l==1){
                JOptionPane.showMessageDialog(null, "Successfully logged in !");
                que1Frame qf = new que1Frame();
                qf.setSize(500, 500);
                qf.setVisible(true);
            }
            else{
                JOptionPane.showMessageDialog(null, "Incorrect Detail !");
            }
        }
        catch(SQLException g){
            Logger.getLogger(QuizFrame.class.getName()).log(Level.SEVERE,null,g);
        }
